package List;

public class Employee {
	String name;
	int age;
	String dep;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getDepartment() {
		return dep;
	}
	public void setDepartment(String dep) {
		this.dep = dep;
	}
	

}
